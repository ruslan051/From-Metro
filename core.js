function createUserData() {
    const getRandomName = (gender) => {
        const names = gender === 'male' ? CONSTANTS.MALE_NAMES : CONSTANTS.FEMALE_NAMES;
        return names[Math.floor(Math.random() * names.length)];
    };

    const randomName = getRandomName(AppState.selectedGender);
    
    return {
        name: randomName,
        station: '',
        wagon: '',
        color: '',
        colorCode: getRandomColor(),
        status: '–í —Ä–µ–∂–∏–º–µ –æ–∂–∏–¥–∞–Ω–∏—è',
        timer: "00:00",
        online: true,
        city: AppState.selectedCity,
        gender: AppState.selectedGender,
        position: '',
        mood: '',
        isWaiting: true,
        isConnected: false
    };
}

/**
 * –í–∞–ª–∏–¥–∞—Ü–∏—è –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
 */
function validateUserData(userData) {
    const required = ['name', 'city', 'gender'];
    const missing = required.filter(field => !userData[field]);
    
    if (missing.length > 0) {
        throw new Error(`–û—Ç—Å—É—Ç—Å—Ç–≤—É—é—Ç –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–µ –ø–æ–ª—è: ${missing.join(', ')}`);
    }
    
    return {
        ...userData,
        name: userData.name.trim() || '–ê–Ω–æ–Ω–∏–º',
        station: userData.station || '',
        wagon: userData.wagon || '',
        color: userData.color || '–°–∏–Ω–∏–π',
        status: userData.status || '–û–∂–∏–¥–∞–Ω–∏–µ'
    };
}

